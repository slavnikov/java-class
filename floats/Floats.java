public class Floats {
   public static void main(String[] args) {
     int intValue = 5; // => 5
     int thirdInt = 5 / 3; // => 1
     float floatValue = 5f; // => 5.0
     float thirdFload = 5f / 3f;
     double doubleValue = 5d; // => 5.0
     double thirdDouble = 5d / 3d;

     System.out.println(thirdInt);
     System.out.println(thirdFload);
     System.out.println(thirdDouble);

     int pounds = 5;
     double kilograms = pounds * 0.453;

     System.out.println(kilograms);
   }
}
